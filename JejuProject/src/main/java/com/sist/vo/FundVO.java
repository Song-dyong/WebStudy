package com.sist.vo;

public class FundVO {
	private int fno;
	private String title, main_poster,crowd_money, end_day, company, archive_rate, link;
	
	
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public int getFno() {
		return fno;
	}
	public void setFno(int fno) {
		this.fno = fno;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getMain_poster() {
		return main_poster;
	}
	public void setMain_poster(String main_poster) {
		this.main_poster = main_poster;
	}
	public String getCrowd_money() {
		return crowd_money;
	}
	public void setCrowd_money(String crowd_money) {
		this.crowd_money = crowd_money;
	}
	public String getEnd_day() {
		return end_day;
	}
	public void setEnd_day(String end_day) {
		this.end_day = end_day;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getArchive_rate() {
		return archive_rate;
	}
	public void setArchive_rate(String archive_rate) {
		this.archive_rate = archive_rate;
	}
	
	
}
